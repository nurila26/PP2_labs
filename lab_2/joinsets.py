#The union() method returns a new set with all items from both sets.
set1 = {"a", "b", "c"}
set2 = {1, 2, 3}

set3 = set1.union(set2)
print(set3)

#You can use the | operator instead of the union() method, and you will get the same result.
set = {"a", "b", "c"}
set0 = {1, 2, 3}

set00 = set | set0
print(set00)

